/**
 * タグの英語名から表示用日本語ラベルを返す。
 * 未登録のタグはそのまま返す（フォールバック）。
 */
export function getTagLabel(name) {
  if (!name) return name
  return TAG_TRANSLATIONS[name] ?? name
}

/**
 * TagSelector の検索：英語名・日本語ラベル両方にマッチさせる
 */
export function tagMatchesSearch(name, searchLower) {
  if (!searchLower) return true
  return (
    name.toLowerCase().includes(searchLower) ||
    (TAG_TRANSLATIONS[name] || '').includes(searchLower)
  )
}
